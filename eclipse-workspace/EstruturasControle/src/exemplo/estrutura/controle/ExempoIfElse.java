package exemplo.estrutura.controle;

public class ExempoIfElse {
	// criando o metrodo principal
	public static void main(String[] args) {
		// criar uma variavel do tipo int
		
		int f = 23;
		// estrutura de decisao dupla
		if(f < 22) {
			System.out.println("f é menor do que 22 e vale : " + f);
		}else {
			System.out.println("f é maior que 22 e vale : " + f);
		}
	}

}
