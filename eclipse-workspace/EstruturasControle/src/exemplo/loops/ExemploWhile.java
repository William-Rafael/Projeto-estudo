package exemplo.loops;

public class ExemploWhile {

	public static void main(String[] args) {
		//criando uma variavel para construção do loop while
		
		int num = 0;
		
		//contruindo o loop
		while(num < 10) {
			//bloco que sera executado enquanto a condicao for verdadeira
			num++;
			System.out.println("O valor de num é:" + num);
			
		}
		

	}

}
